<?php
# -----------------------------------------------------------------
# 投稿(POST)の管理画面での表記を修正
# -----------------------------------------------------------------
function tsbk_change_label_admin_menu() {
	global $menu;
	global $submenu;
	$name = 'ブログ';
	$menu[5][0] = $name;
	$submenu['edit.php'][5][0] = $name.'一覧';
	$submenu['edit.php'][10][0] = '新しい'.$name;
}
add_action( 'admin_menu', 'tsbk_change_label_admin_menu');

function tsbk_change_label_object() {
	global $wp_post_types;
	$name = 'ブログ';
	$labels = &$wp_post_types['post']->labels;
	$labels->name = $name;
	$labels->singular_name = $name;
	$labels->add_new = _x('追加', $name);
	$labels->add_new_item = $name.'の新規追加';
	$labels->edit_item = $name.'の編集';
	$labels->new_item = '新規'.$name;
	$labels->view_item = $name.'を表示';
	$labels->search_items = $name.'を検索';
	$labels->not_found = $name.'が見つかりませんでした';
	$labels->not_found_in_trash = 'ゴミ箱に'.$name.'は見つかりませんでした';
	$wp_post_types['post']->label = $name;
}
add_action( 'init', 'tsbk_change_label_object' );

